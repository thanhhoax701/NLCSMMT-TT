package com.example.appweather;

public class History {
    private String imgTrangThaiHW;
    private String detailDayHW, detailHW, nhietDoHW, trangThaiHW, gioHW, apSuatHW, doAmHW, chiSoUVHW;

    public History(String imgTrangThaiHW, String detailDayHW, String detailHW, String nhietDoHW, String trangThaiHW, String gioHW, String apSuatHW, String doAmHW, String chiSoUVHW) {
        this.imgTrangThaiHW = imgTrangThaiHW;
        this.detailDayHW = detailDayHW;
        this.detailHW = detailHW;
        this.nhietDoHW = nhietDoHW;
        this.trangThaiHW = trangThaiHW;
        this.gioHW = gioHW;
        this.apSuatHW = apSuatHW;
        this.doAmHW = doAmHW;
        this.chiSoUVHW = chiSoUVHW;
    }

    public String getImgTrangThaiHW() {
        return imgTrangThaiHW;
    }

    public void setImgTrangThaiHW(String imgTrangThaiHW) {
        this.imgTrangThaiHW = imgTrangThaiHW;
    }

    public String getDetailDayHW() {
        return detailDayHW;
    }

    public void setDetailDayHW(String detailDayHW) {
        this.detailDayHW = detailDayHW;
    }

    public String getDetailHW() {
        return detailHW;
    }

    public void setDetailHW(String detailHW) {
        this.detailHW = detailHW;
    }

    public String getNhietDoHW() {
        return nhietDoHW;
    }

    public void setNhietDoHW(String nhietDoHW) {
        this.nhietDoHW = nhietDoHW;
    }

    public String getTrangThaiHW() {
        return trangThaiHW;
    }

    public void setTrangThaiHW(String trangThaiHW) {
        this.trangThaiHW = trangThaiHW;
    }

    public String getGioHW() {
        return gioHW;
    }

    public void setGioHW(String gioHW) {
        this.gioHW = gioHW;
    }

    public String getApSuatHW() {
        return apSuatHW;
    }

    public void setApSuatHW(String apSuatHW) {
        this.apSuatHW = apSuatHW;
    }

    public String getDoAmHW() {
        return doAmHW;
    }

    public void setDoAmHW(String doAmHW) {
        this.doAmHW = doAmHW;
    }

    public String getChiSoUVHW() {
        return chiSoUVHW;
    }

    public void setChiSoUVHW(String chiSoUVHW) {
        this.chiSoUVHW = chiSoUVHW;
    }
}
